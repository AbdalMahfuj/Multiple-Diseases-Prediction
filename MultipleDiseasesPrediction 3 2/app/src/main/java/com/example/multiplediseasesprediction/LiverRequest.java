package com.example.multiplediseasesprediction;

public class LiverRequest {
    private String Age, Gender, TotalBilirubin, DirectBilirubin, AlkalinePhosphatase, AlanineAminotransferase, AsparateAminotransferase, TotalProtein, Albumin, AlbuminGlobulinRatio;

    public String getAge() { return Age; }
    public void setAge(String age) { Age = age ; }

    public String getGender() { return Gender; }
    public void setGender(String gender) { Gender = gender ; }

    public String getTotalBilirubin() { return TotalBilirubin; }
    public void setTotalBilirubin(String totalBilirubin) { TotalBilirubin = totalBilirubin ; }

    public String getDirectBilirubin() { return DirectBilirubin; }
    public void setDirectBilirubin(String directBilirubin) { DirectBilirubin = directBilirubin ; }

    public String getAlkalinePhosphatase() { return AlkalinePhosphatase; }
    public void setAlkalinePhosphatase(String alkalinePhosphatase) { AlkalinePhosphatase = alkalinePhosphatase ; }

    public String getAlanineAminotransferase() { return AlanineAminotransferase; }
    public void setAlanineAminotransferase(String alamineAminotransferase) { AlanineAminotransferase = alamineAminotransferase ; }

    public String getAsparateAminotransferase() { return AsparateAminotransferase; }
    public void setAsparateAminotransferase(String asparateAminotransferase) { AsparateAminotransferase = asparateAminotransferase ; }

    public String getTotalProtein() { return TotalProtein; }
    public void setTotalProtein(String totalProtein) { TotalProtein = totalProtein ; }

    public String getAlbumin() { return Albumin; }
    public void setAlbumin(String albumin) { Albumin = albumin ; }

    public String getAlbuminGlobulinRatio() { return AlbuminGlobulinRatio; }
    public void setAlbuminGlobulinRatio(String albuminGlobulinRatio) { AlbuminGlobulinRatio = albuminGlobulinRatio ; }

}
