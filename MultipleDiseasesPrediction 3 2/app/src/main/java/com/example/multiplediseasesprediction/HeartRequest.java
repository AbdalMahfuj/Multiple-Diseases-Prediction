package com.example.multiplediseasesprediction;

public class HeartRequest {
    private String age, gender, height, weight, sbp, dbp, cholesterol, glucose, smoking, alcohol, phys_activity;

    public String getAge() { return age; }
    public void setAge(String Age) { age = Age ; }

    public String getGender() { return gender ; }
    public void setGender(String Gender) { gender = Gender ; }

    public String getHeight() { return height ; }
    public void setHeight(String Height) { height = Height ; }

    public String getWeight() { return weight ; }
    public void setWeight(String Weight) { weight = Weight ; }

    public String getSbp() { return sbp ; }
    public void setSbp(String Sbp) { sbp = Sbp ; }

    public String getDbp() { return dbp ; }
    public void setDbp(String Dbp) { dbp = Dbp ; }

    public String getCholesterol() { return cholesterol ; }
    public void setCholesterol(String Cholesterol) { cholesterol = Cholesterol ; }

    public String getGlucose() { return glucose ; }
    public void setGlucose(String Glucose) { glucose = Glucose ; }

    public String getSmoking() { return smoking ; }
    public void setSmoking(String Smoking) { smoking = Smoking ; }

    public String getAlcohol() { return alcohol ; }
    public void setAlcohol(String Alcohol) { alcohol = Alcohol ; }

    public String getPhys_activity() { return phys_activity ; }
    public void setPhys_activity(String Phys_activity) { phys_activity = Phys_activity ; }

}
