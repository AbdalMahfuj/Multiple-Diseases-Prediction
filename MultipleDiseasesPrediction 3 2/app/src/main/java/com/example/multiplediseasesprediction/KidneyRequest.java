package com.example.multiplediseasesprediction;

public class KidneyRequest {
    private String age, bp, wbc, rbc, appetite, pusCell, hypertension, hemoglobin, blood_glucoseRandom, diabetesMellitus, anemia;

    public String getAge() { return age; }
    public void setAge(String Age) { age = Age ; }

    public String getBp() { return bp; }
    public void setBp(String Bp) { bp = Bp ; }

    public String getWbc() { return wbc; }
    public void setWbc(String Wbc) { wbc = Wbc ; }

    public String getRbc() { return rbc; }
    public void setRbc(String Rbc) { rbc = Rbc ; }

    public String getAppetite() { return appetite; }
    public void setAppetite(String Appetite) { appetite = Appetite ; }

    public String getPusCell() { return pusCell; }
    public void setPusCell(String PusCell) { pusCell = PusCell ; }

    public String getHypertension() { return hypertension; }
    public void setHypertension(String Hypertension) { hypertension = Hypertension ; }

    public String getHemoglobin() { return hemoglobin; }
    public void setHemoglobin(String Hemoglobin) { hemoglobin = Hemoglobin ; }

    public String getBlood_glucoseRandom() { return blood_glucoseRandom; }
    public void setBlood_glucoseRandom(String Blood_glucoseRandom) { blood_glucoseRandom = Blood_glucoseRandom ; }

    public String getDiabetesMellitus() { return diabetesMellitus; }
    public void setDiabetesMellitus(String DiabetesMellitus) { diabetesMellitus = DiabetesMellitus ; }

    public String getAnemia() { return anemia; }
    public void setAnemia(String Anemia) { anemia = Anemia ; }

}
